int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (j < nb && i <= 46341)
	{
		j = i * i;
		i++;
	}
	if (j == nb)
		return (i - 1);
	return (0);
}

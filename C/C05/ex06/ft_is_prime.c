int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	if (nb % 2 == 0)
		return (0);
	while (nb % i != 0 && i <= nb / 2)
		i += 2;
	if (nb / i == 1)
		return (1);
	else
		return (0);
}
